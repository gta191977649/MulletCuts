import java.util.*;
public class In
{
    static Scanner sc = new Scanner(System.in);

    static char nextChar()
    {
        return sc.next().charAt(0);
    }
    static String next()
    {
        return sc.next();
    }
    static double nextDouble()
    {
        return sc.nextDouble();
    }
    static int nextInt()
    {
        return sc.nextInt();
    }
    
}
